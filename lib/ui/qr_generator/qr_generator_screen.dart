  // --- PDF GENERATION ---
  Future<void> _generateAndPrintPdf() async {
    showDialog(
      context: context,
      barrierDismissible: false,
      builder: (context) => const Center(child: CircularProgressIndicator()),
    );

    try {
      await Future.delayed(const Duration(milliseconds: 100));

      final imageBytes = await _screenshotController.capture(
          pixelRatio: MediaQuery.of(context).devicePixelRatio);

      if (imageBytes == null) {
        if (mounted) Navigator.pop(context);
        return;
      }

      final pdf = pw.Document();
      final qrImage = pw.MemoryImage(imageBytes);

      pdf.addPage(
        pw.Page(
          pageFormat: PdfPageFormat.a4,
          build: (pw.Context context) {
            return pw.Stack(
              children: [
                pw.Center(
                  child: pw.Opacity(
                    opacity: 0.1,
                    child: pw.Text(
                      'QR S&G',
                      style: pw.TextStyle(
                        fontSize: 80,
                        fontWeight: pw.FontWeight.bold,
                        fontStyle: pw.FontStyle.italic,
                      ),
                    ),
                  ),
                ),
                pw.Center(
                  child: pw.Column(
                    mainAxisAlignment: pw.MainAxisAlignment.center,
                    children: [
                      pw.Text('QR Code Result',
                          style: pw.TextStyle(
                              fontSize: 24, fontWeight: pw.FontWeight.bold)),
                      pw.SizedBox(height: 30),
                      pw.Container(
                        padding: const pw.EdgeInsets.all(20),
                        decoration: pw.BoxDecoration(
                          border: pw.Border.all(width: 2),
                          borderRadius: pw.BorderRadius.circular(10),
                        ),
                        child: pw.Image(qrImage, width: 250, height: 250),
                      ),
                      pw.SizedBox(height: 30),
                      pw.Text('Data:',
                          style: pw.TextStyle(
                              fontSize: 16, color: PdfColors.grey700)),
                      pw.SizedBox(height: 5),
                      pw.Text(_qrData ?? '-',
                          style: pw.TextStyle(
                              fontSize: 18, fontWeight: pw.FontWeight.bold)),
                      pw.Spacer(),
                      pw.Text('Generated by QR S&G App',
                          style: pw.TextStyle(
                              fontSize: 12, color: PdfColors.grey)),
                    ],
                  ),
                ),
              ],
            );
          },
        ),
      );

      if (!kIsWeb) {
        String? path;
        try {
          if (Platform.isAndroid) {
            path =
                '/storage/emulated/0/Download/QR_${DateTime.now().millisecondsSinceEpoch}.pdf';
          } else {
            final dir = await getDownloadsDirectory();
            if (dir != null) {
              path =
                  '${dir.path}/QR_${DateTime.now().millisecondsSinceEpoch}.pdf';
            }
          }

          if (path != null) {
            final file = File(path);
            await file.writeAsBytes(await pdf.save());

            // History
            await HistoryService().addToHistory(HistoryItem(
              id: DateTime.now().toString(),
              type: HistoryType.create,
              data: _qrData!,
              timestamp: DateTime.now(),
            ));

            if (mounted) Navigator.pop(context);

            if (mounted) {
              ScaffoldMessenger.of(context).showSnackBar(
                SnackBar(
                  content: Text('PDF Saved: $path'),
                  backgroundColor: Colors.green,
                  action: SnackBarAction(
                    label: 'OPEN',
                    textColor: Colors.white,
                    onPressed: () => OpenFile.open(path),
                  ),
                ),
              );
            }
            await OpenFile.open(path);
            return;
          }
        } catch (e) {
          debugPrint('Failed to save file: $e');
        }
      }

      if (mounted) Navigator.pop(context);
      await Printing.layoutPdf(
        onLayout: (format) async => pdf.save(),
        name: 'QR_Code_${DateTime.now().millisecondsSinceEpoch}.pdf',
      );
    } catch (e) {
      if (mounted) Navigator.pop(context);
      ScaffoldMessenger.of(context).showSnackBar(
        SnackBar(content: Text('Error: $e'), backgroundColor: Colors.red),
      );
    }
  }

  // --- PNG SAVING ---
  Future<void> _savePng() async {
    if (_qrData == null || _qrData!.isEmpty) {
      _showErrorSnackBar(context);
      return;
    }

    showDialog(
      context: context,
      barrierDismissible: false,
      builder: (context) => const Center(child: CircularProgressIndicator()),
    );

    try {
      await Future.delayed(const Duration(milliseconds: 100));
      final imageBytes = await _screenshotController.capture(pixelRatio: 3.0);

      if (imageBytes == null) {
        if (mounted) Navigator.pop(context);
        return;
      }

      if (kIsWeb) {
        if (mounted) {
          Navigator.pop(context);
          ScaffoldMessenger.of(context).showSnackBar(
            const SnackBar(
                content: Text('Use Share feature for Web download.'),
                backgroundColor: Colors.orange),
          );
        }
        return;
      }

      String? path;
      if (Platform.isAndroid) {
        path =
            '/storage/emulated/0/Download/QR_${DateTime.now().millisecondsSinceEpoch}.png';
      } else {
        final dir = await getDownloadsDirectory();
        if (dir != null) {
          path = '${dir.path}/QR_${DateTime.now().millisecondsSinceEpoch}.png';
        }
      }

      if (path != null) {
        final file = File(path);
        await file.writeAsBytes(imageBytes);

        // History
        await HistoryService().addToHistory(HistoryItem(
          id: DateTime.now().toString(),
          type: HistoryType.create,
          data: _qrData!,
          timestamp: DateTime.now(),
        ));

        if (mounted) Navigator.pop(context);

        if (mounted) {
          ScaffoldMessenger.of(context).showSnackBar(
            SnackBar(
              content: Text('PNG Saved: $path'),
              backgroundColor: Colors.green,
              action: SnackBarAction(
                label: 'OPEN',
                textColor: Colors.white,
                onPressed: () => OpenFile.open(path),
              ),
            ),
          );
        }
        await OpenFile.open(path);
      } else {
        if (mounted) Navigator.pop(context);
      }
    } catch (e) {
      if (mounted) Navigator.pop(context);
      if (mounted) {
        ScaffoldMessenger.of(context).showSnackBar(
            SnackBar(content: Text('Error: $e'), backgroundColor: Colors.red));
      }
    }
  }

  // --- ERROR SNACKBAR ---
  void _showErrorSnackBar(BuildContext context) {
    if (_overlayEntry == null) {
      _overlayEntry = OverlayEntry(builder: (context) {
        return Positioned(
          top: MediaQuery.of(context).padding.top + 10,
          right: 20,
          width: MediaQuery.of(context).size.width * 0.5,
          child: Material(
            color: Colors.transparent,
            child: ValueListenableBuilder<List<ToastData>>(
              valueListenable: _toastQueue,
              builder: (context, toasts, child) {
                return Column(
                  crossAxisAlignment: CrossAxisAlignment.end,
                  mainAxisSize: MainAxisSize.min,
                  children: toasts.map((t) => ToastWidget(
                        key: ValueKey(t.id),
                        data: t,
                        onDismiss: () {
                          final list = List<ToastData>.from(_toastQueue.value);
                          list.removeWhere((item) => item.id == t.id);
                          _toastQueue.value = list;
                        },
                      )).toList(),
                );
              },
            ),
          ),
        );
      });
      Overlay.of(context).insert(_overlayEntry!);
    }

    final list = List<ToastData>.from(_toastQueue.value);
    if (list.length >= 3) {
      list.removeAt(0);
    }
    list.add(ToastData('Oops! Konten kosong.\nIsi dulu yuk!'));
    _toastQueue.value = list;
  }

